package com.macys.sdt.framework.model.registry;

/**
 * Represents a macy's store
 */
public class Store {

    private int storeId = 139;
    private int storeDivId = 71;
    private int locationNumber = 5467;

    public int getStoreId() {
        return storeId;
    }

    public void setStoreId(int storeId) {
        this.storeId = storeId;
    }

    public int getStoreDivId() {
        return storeDivId;
    }

    public void setStoreDivId(int storeDivId) {
        this.storeDivId = storeDivId;
    }

    public int getLocationNumber() {
        return locationNumber;
    }

    public void setLocationNumber(int locationNumber) {
        this.locationNumber = locationNumber;
    }

}
